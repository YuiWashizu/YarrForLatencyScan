#ifndef KU040REGISTERS_H__
#define KU040REGISTERS_H__


// define base addresses
#define KU040_PIXEL_BASE			0x1000
#define KU040_PIXEL_TX_BASE			(KU040_PIXEL_BASE)
#define KU040_PIXEL_RX_BASE			((KU040_PIXEL_BASE) + 20*16)
#define KU040_PIXEL_DEBUG_BASE		((KU040_PIXEL_BASE) + 40*16)
#define KU040_PIXEL_TRIGGEN_BASE	((KU040_PIXEL_BASE) + 41*16)

// TX registers (relative to channel)
#define KU040_PIXEL_TX_CONTROL(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 0)
#define KU040_PIXEL_TX_STATUS(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 1)
#define KU040_PIXEL_TX_FIFO(ch)		((KU040_PIXEL_TX_BASE) + 16 * (ch) + 2)
#define KU040_PIXLE_TX_CNT_TRIG(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 3)
#define KU040_PIXLE_TX_CNT_ECR(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 4)
#define KU040_PIXLE_TX_CNT_BCR(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 5)
#define KU040_PIXLE_TX_CNT_CAL(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 6)
#define KU040_PIXLE_TX_CNT_SLOW(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 7)
#define KU040_PIXLE_TX_CNT_CORRUPT(ch)	((KU040_PIXEL_TX_BASE) + 16 * (ch) + 8)

// RX registers
#define KU040_PIXEL_RX_CONTROL(ch)		((KU040_PIXEL_RX_BASE) + 16 * (ch) + 0)
#define KU040_PIXEL_RX_STATUS(ch)		((KU040_PIXEL_RX_BASE) + 16 * (ch) + 1)
#define KU040_PIXEL_RX_FIFO(ch)			((KU040_PIXEL_RX_BASE) + 16 * (ch) + 2)
#define KU040_PIXEL_RX_FIFOCNT(ch)		((KU040_PIXEL_RX_BASE) + 16 * (ch) + 3)
#define KU040_PIXEL_RX_CNT_DECERR(ch)	((KU040_PIXEL_RX_BASE) + 16 * (ch) + 4)
#define KU040_PIXEL_RX_CNT_DISPERR(ch)	((KU040_PIXEL_RX_BASE) + 16 * (ch) + 5)
#define KU040_PIXEL_RX_CNT_SOF(ch)		((KU040_PIXEL_RX_BASE) + 16 * (ch) + 6)
#define KU040_PIXEL_RX_CNT_EVENT(ch)	((KU040_PIXEL_RX_BASE) + 16 * (ch) + 7)

// debug registers
#define KU040_PIXEL_DEBUG_SELECT		((KU040_PIXEL_DEBUG_BASE) + 0)
#define KU040_PIXEL_DEBUG_EMU_ENABLE	((KU040_PIXEL_DEBUG_BASE) + 1)
#define KU040_PIXEL_DEBUG_EMU_HITDIST	((KU040_PIXEL_DEBUG_BASE) + 2)
#define KU040_PIXEL_DEBUG_EMU_HITCONF	((KU040_PIXEL_DEBUG_BASE) + 3)

// trigger generator
#define KU040_PIXEL_TRIGGEN_CONTROL		((KU040_PIXEL_TRIGGEN_BASE) + 0)
#define KU040_PIXEL_TRIGGEN_STATUS		((KU040_PIXEL_TRIGGEN_BASE) + 1)
#define KU040_PIXEL_TRIGGEN_WORD1		((KU040_PIXEL_TRIGGEN_BASE) + 4)
#define KU040_PIXEL_TRIGGEN_WORD2		((KU040_PIXEL_TRIGGEN_BASE) + 5)
#define KU040_PIXEL_TRIGGEN_WORD3		((KU040_PIXEL_TRIGGEN_BASE) + 6)
#define KU040_PIXEL_TRIGGEN_WORD4		((KU040_PIXEL_TRIGGEN_BASE) + 7)
#define KU040_PIXEL_TRIGGEN_FREQ		((KU040_PIXEL_TRIGGEN_BASE) + 8)
#define KU040_PIXEL_TRIGGEN_COUNT		((KU040_PIXEL_TRIGGEN_BASE) + 9)
#define KU040_PIXEL_TRIGGEN_TIME1		((KU040_PIXEL_TRIGGEN_BASE) + 10)
#define KU040_PIXEL_TRIGGEN_TIME2		((KU040_PIXEL_TRIGGEN_BASE) + 11)

#endif // KU040REGISTERS_H

